const NUM = 25

const HASHTAG = '# '
const DOT = '. '

for (let i = 0; i < NUM; i++) {
  for (let j = 0; j < NUM; j++) {
    process.stdout.write(condiction(i, j) ? HASHTAG : DOT)
  }

  process.stdout.write("\n")
}


function condiction(i, j) {
  const frameOffset = 2

  return i > frameOffset && i < NUM - frameOffset - 1 && j > frameOffset && j < NUM - frameOffset - 1 &&
    i - j < 10 && j - i < 10 && i + j < 34 && i + j > 14
}
