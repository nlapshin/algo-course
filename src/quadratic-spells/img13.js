const NUM = 25

const HASHTAG = '# '
const DOT = '. '

for (let i = 0; i < NUM; i++) {
  for (let j = 0; j < NUM; j++) {
    process.stdout.write(condiction(i, j) ? HASHTAG : DOT)
  }

  process.stdout.write("\n")
}


function condiction(i, j) {
  const leftOffset = NUM - 6
  const rightOffset = NUM + 5

  return i + j > leftOffset && i + j < rightOffset
}

