const NUM = 25

const HASHTAG = '# '
const DOT = '. '

for (let i = 0; i < NUM; i++) {
  let step = i

  for (let j = 0; j < NUM; j++) {
    process.stdout.write(condiction(i, j, step) ? HASHTAG : DOT)
  }

  process.stdout.write("\n")
}


function condiction(i, j, step) {
  return i + 1 <= j && i + 1 >= j - step
}

